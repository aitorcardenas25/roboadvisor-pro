export function getProductById(id: string): FinancialProduct | undefined {
  return FINANCIAL_PRODUCTS.find(p => p.id === id);
}

export function getProductsByAssetClass(assetClass: AssetClass): FinancialProduct[] {
  return FINANCIAL_PRODUCTS.filter(p => p.assetClass === assetClass);
}

export function getProductsByProfile(profile: InvestorProfile): FinancialProduct[] {
  return FINANCIAL_PRODUCTS.filter(p => p.recommendedProfiles.includes(profile));
}

export function getDataStatusLabel(status: DataStatus): string {
  const labels: Record<DataStatus, string> = {
    validated: '✅ Dades validades',
    partial: '⚠️ Dades parcials',
    pending: '🕐 Pendent de dades',
    unavailable: '❌ Dades no disponibles',
  };
  return labels[status];
}

export function getDataStatusColor(status: DataStatus): string {
  const colors: Record<DataStatus, string> = {
    validated:   'bg-green-100 text-green-800',
    partial:     'bg-yellow-100 text-yellow-800',
    pending:     'bg-blue-100 text-blue-800',
    unavailable: 'bg-red-100 text-red-800',
  };
  return colors[status];
}

export function getSFDRLabel(article?: 6 | 8 | 9): string {
  if (!article) return 'No classificat';
  const labels = { 6: 'Art. 6 — Sense objectiu sostenible', 8: 'Art. 8 — Promou criteris ESG', 9: 'Art. 9 — Objectiu sostenible' };
  return labels[article];
}

export function getSFDRColor(article?: 6 | 8 | 9): string {
  if (!article) return 'bg-gray-100 text-gray-600';
  const colors = { 6: 'bg-gray-100 text-gray-700', 8: 'bg-blue-100 text-blue-800', 9: 'bg-green-100 text-green-800' };
  return colors[article];
}

export function getMorningstarStars(rating?: number): string {
  if (!rating) return '—';
  return '★'.repeat(rating) + '☆'.repeat(5 - rating);
}

export function getRiskIndicatorLabel(sriRisk?: number): string {
  if (!sriRisk) return '—';
  const labels: Record<number, string> = { 1: 'SRI 1 - Molt Baix', 2: 'SRI 2 - Baix', 3: 'SRI 3 - Mig-Baix', 4: 'SRI 4 - Mig', 5: 'SRI 5 - Mig-Alt', 6: 'SRI 6 - Alt', 7: 'SRI 7 - Molt Alt' };
  return labels[sriRisk] ?? `SRI ${sriRisk}`;
}

export function getProductStats() {
  const total    = FINANCIAL_PRODUCTS.length;
  const byClass  = FINANCIAL_PRODUCTS.reduce<Record<string, number>>((acc, p) => {
    acc[p.assetClass] = (acc[p.assetClass] ?? 0) + 1;
    return acc;
  }, {});
  const avgTER   = FINANCIAL_PRODUCTS.reduce((s, p) => s + p.ter, 0) / total;
  const withESG  = FINANCIAL_PRODUCTS.filter(p => p.sfdrArticle && p.sfdrArticle >= 8).length;
  const indexed  = FINANCIAL_PRODUCTS.filter(p => p.managementType === 'indexada').length;
  return { total, byClass, avgTER: Math.round(avgTER * 1000) / 1000, withESG, indexed };
}